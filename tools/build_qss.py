# ---------------------------------------------------------------------------
# Renderer
# ---------------------------------------------------------------------------


def render_qss(theme: Theme) -> str:
    """Produce the QSS string for one theme."""
    # Validate up-front so a missing token gives a clear error rather
    # than ``KeyError`` from inside ``str.format_map``.
    missing = [k for k in TOKEN_KEYS if k not in theme.tokens]
    if missing:
        raise ValueError(
            f"theme {theme.id!r} missing tokens: {missing}"
        )

    return _QSS_TEMPLATE.format(
        id=theme.id,
        name=theme.name,
        family=theme.family,
        notes=theme.notes,
        tokens=theme.tokens,
    )


def write_all(qss_dir: Path) -> list[Path]:
    """Render every theme and write to ``qss_dir/{id}.qss``.

    Returns the list of paths written.
    """
    qss_dir.mkdir(parents=True, exist_ok=True)
    written: list[Path] = []
    for theme in THEMES:
        out = qss_dir / f"{theme.id}.qss"
        out.write_text(render_qss(theme), encoding="utf-8")
        written.append(out)
    return written


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------


def main(argv: list[str] | None = None) -> int:
    qss_dir = _REPO_ROOT / "gui_qt" / "qss"
    paths = write_all(qss_dir)
    print(f"Wrote {len(paths)} QSS files under {qss_dir}:")
    for p in paths:
        size = p.stat().st_size
        print(f"  - {p.name}  ({size:,} bytes)")
    return 0


if __name__ == "__main__":
    sys.exit(main())
