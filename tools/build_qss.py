"""Render the 6 PySide6 themes from Python tokens to QSS files.

Usage:

    python tools/build_qss.py

Reads ``THEMES`` from ``gui_qt.themes`` (the Python source of truth)
and writes one ``gui_qt/qss/{theme_id}.qss`` file per theme.

This is a **dev-time** tool, not a runtime dependency.  The committed
QSS files are the runtime artifacts — ``gui_qt/theme.py``'s
``load_theme()`` reads them directly.  Re-run this script whenever
``gui_qt/themes.py`` changes.

Why this split: tokens in Python are testable (WCAG pins, key
coverage, etc.) and easy to diff per theme.  QSS files on disk are
what Qt's ``setStyleSheet`` consumes.  Generated artifacts stay in
the repo so a fresh checkout doesn't need this script just to launch.
"""

